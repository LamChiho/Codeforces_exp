void solve() {
    int m,n,l,r;
    cin>>m>>n>>l>>r;
    while(m>n){
        if(l<0)l++;
        else if(r>0){
            r--;
        }
        m--;
    }
    cout<<l<<' '<<r<<"\n";
}


